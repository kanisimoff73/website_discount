                            cat_id_ = categories_id[cat]
                            shop = shops_id[shop_name]
                            Products.objects.create(
                                name= name,
                                photo= path_to_photo,
                                previous_price= previous_price,
                                link= link,
                                cat_id=cat_id_,
                                shop_id= shop,
                            )
